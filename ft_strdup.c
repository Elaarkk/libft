#include <stdlib.h>

char	*ft_strdup(const char *s)
{
	char	*cpy;
	int		len;
	int		i;

	i = 0;
	len = 0;
	while (s[len])
		len++;
	cpy = malloc(sizeof(char) * len + 1);
	if (!cpy)
		return (0);
	while (s[i])
	{
		cpy[i] = s[i];
		i++;
	}
	cpy[i] = '\0';
	return (cpy);
}
